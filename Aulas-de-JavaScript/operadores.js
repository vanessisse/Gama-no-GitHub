/*

Operadores aritméticos:

Adição: +
Subtração: -
Multiplicação: *
Divisão: /
Módulo ou resto de uma divisão: %
Incremento: ++
Decremento: --

*/

var sumTwoNumbers = 1 + 6
console.log(sumTwoNumbers)

var subTwoNumbers = 4 - 2
console.log(sumTwoNumbers)

var multTwoNumbers = 5 * 2
console.log(multTwoNumbers)

var divTwoNumbers = 4 / 2
console.log(divTwoNumbers)

console.log(8 % 4)

var num = 1;
console.log(++num) //num = num + 1

var num = 2;
console.log(--num) //num = num - 1

/*

Operadores de atribuição:

Atribui-se usando o símbolo "="

*/

var x = 4
var y = 2
console.log(x += y) //x = x + y
console.log(x -= y)
console.log(x *= y)
console.log(x /= y)
console.log(x %= y)

